vegetables = ["Avocados", "Sweet corn", "Pineapples", "Cabbages", "Onions", "Papayas"]

dirty_dozen = [fruits, vegetables]

print(dirty_dozen)

#vegetables is at the index 1 and in that list sweet corn is at index 1
print(dirty_dozen[1][1])